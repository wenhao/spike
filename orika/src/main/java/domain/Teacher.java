package domain;

public class Teacher
{
    private String name;
    private int age;
    private String diff2;
    private String desc;

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(final int age) {
        this.age = age;
    }

    public String getDiff2() {
        return diff2;
    }

    public void setDiff2(final String diff2) {
        this.diff2 = diff2;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(final String desc) {
        this.desc = desc;
    }
}
